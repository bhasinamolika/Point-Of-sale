    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String OldPassword=jPasswordField1.getText();
        String NewPassword=jPasswordField2.getText();
        String ConfirmPassword=jPasswordField3.getText();
        if (Username.isEmpty()|| OldPassword.isEmpty()|| NewPassword.isEmpty()|| ConfirmPassword.isEmpty())
        {
            JOptionPane.showMessageDialog(rootPane,"All fields are required.");
        }
        else if( !NewPassword.equals(ConfirmPassword)){
            JOptionPane.showMessageDialog(rootPane,"Please confirm the new Password");
        }
        else{
            try{
                ResultSet rs =DbLoader.executestatment("Select * from cashier where username='"+Username+"' and password='"+OldPassword+"'");
                if (rs.next()){
                   rs.moveToCurrentRow();
                   rs.updateString("password", NewPassword);
                   rs.updateRow();
                   JOptionPane.showMessageDialog(rootPane, "password changed");
                }
                else{
                    JOptionPane.showMessageDialog(rootPane, "Invalid old password");
                }
            }
            catch(Exception e){
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

